    def compute_observations(self):
        self._refresh()
        obs = ["eef_pos", "eef_quat"]
        self.obs_buf = torch.cat([self.states[ob] for ob in obs], dim=-1)

        return self.obs_buf

    def reset_idx(self, env_ids):
        # Reset agent
        reset_noise = torch.rand((len(env_ids), 7), device=self.device)
        pos = tensor_clamp(
            self.franka_default_dof_pos.unsqueeze(0) +
            self.franka_dof_noise * 2.0 * (reset_noise - 0.5),
            self.franka_dof_lower_limits.unsqueeze(0), self.franka_dof_upper_limits
        )

        # Reset the internal obs accordingly
        self._q[env_ids, :] = pos
        self._qd[env_ids, :] = torch.zeros_like(self._qd[env_ids])

        # Set effort control to be 0
        # NOTE: Task takes care of actually propagating these controls in sim using the SimActions API
        self._effort_control[env_ids, :] = torch.zeros_like(pos)

        # Deploy updates
        multi_env_ids_int32 = self._global_indices[env_ids, 0].flatten()
        self.gym.set_dof_actuation_force_tensor_indexed(
            self.sim,
            gymtorch.unwrap_tensor(self._effort_control),
            gymtorch.unwrap_tensor(multi_env_ids_int32),
            len(multi_env_ids_int32)
        )
        self.gym.set_dof_state_tensor_indexed(
            self.sim,
            gymtorch.unwrap_tensor(self._dof_state),
            gymtorch.unwrap_tensor(multi_env_ids_int32),
            len(multi_env_ids_int32)
        )
        
        self.progress_buf[env_ids] = 0
        self.reset_buf[env_ids] = 0

    def _compute_osc_torques(self, dpose):
        # Solve for Operational Space Control # Paper: khatib.stanford.edu/publications/pdfs/Khatib_1987_RA.pdf
        # Helpful resource: studywolf.wordpress.com/2013/09/17/robot-control-4-operation-space-control/
        q, qd = self._q[:, :7], self._qd[:, :7]
        mm_inv = torch.inverse(self._mm)
        m_eef_inv = self._j_eef @ mm_inv @ torch.transpose(self._j_eef, 1, 2)
        m_eef = torch.inverse(m_eef_inv)

        # Transform our cartesian action `dpose` into joint torques `u`
        u = torch.transpose(self._j_eef, 1, 2) @ m_eef @ (self.kp * dpose - self.kd * self.states["eef_vel"]).unsqueeze(-1)

        # Nullspace control torques `u_null` prevents large changes in joint configuration
        # They are added into the nullspace of OSC so that the end effector orientation remains constant
        # roboticsproceedings.org/rss07/p31.pdf
        j_eef_inv = m_eef @ self._j_eef @ mm_inv
        u_null = self.kd_null * -qd + self.kp_null * ((self.franka_default_dof_pos[:7] - q + np.pi) % (2 * np.pi) - np.pi)
        u_null[:, 7:] *= 0
        u_null = self._mm @ u_null.unsqueeze(-1)
        u += (torch.eye(7, device=self.device).unsqueeze(0) - torch.transpose(self._j_eef, 1, 2) @ j_eef_inv) @ u_null

        # Clip the values to be within valid effort range
        u = tensor_clamp(u.squeeze(-1), -self._franka_effort_limits[:7].unsqueeze(0), self._franka_effort_limits[:7].unsqueeze(0))

        return u

    def pre_physics_step(self, actions):
        # Control arm (scale value first)
        self.actions = actions.clone().to(self.device)
        dpose = self.actions[:, :6]
        control_params = self.actions[:, 6:]
        if self.control_type == "admittance":
            dpos_prev = dpose[:, :3] # (num_envs, 3)
            vel_prev = self.states['eef_vel'][:, :3] # (num_envs, 3)
            inertia = control_params[:, 0].unsqueeze(-1)  # (num_envs, 1)
            stiffness = control_params[:, 1].unsqueeze(-1)  # (num_envs, 1)
            damping = 2 * torch.sqrt(inertia * stiffness) # (num_envs, 1), Assume critical damping
            for i in range(self.n_compliance):
                force = self._contact_forces[:, self.handles['franka_fingertip']] 
                # Solve x' = f(x) using Euler's method, where x = [dpos, vel] TODO: Use 2nd order Heun's method
                vel = vel_prev
                acc = (force - damping * vel_prev + stiffness * dpos_prev) / inertia
                dpos = dpos_prev + vel * self.dt
                vel = vel_prev + acc * self.dt
                dpos_prev = dpos
                vel_prev = vel
                # Use OSC for position control
                dpose[:, :3] = dpos
                self._effort_control = self._compute_osc_torques(dpose=dpose)

                # Deploy actions
                self.gym.set_dof_actuation_force_tensor(self.sim, gymtorch.unwrap_tensor(self._effort_control))
                if i < self.n_compliance - 1: # Skip the last step Since the last step will be done in step()
                    # step physics and render each frame
                    for i in range(self.control_freq_inv):
                        if self.force_render:
                            self.render()
                        self.gym.simulate(self.sim)
        else:
            if self.control_type == "osc":
                self._effort_control = self._compute_osc_torques(dpose=dpose)

            # Deploy actions
            self.gym.set_dof_actuation_force_tensor(self.sim, gymtorch.unwrap_tensor(self._effort_control))

    def post_physics_step(self):
        self.progress_buf += 1

        env_ids = self.reset_buf.nonzero(as_tuple=False).squeeze(-1)
        if len(env_ids) > 0:
            self.reset_idx(env_ids)

        self.compute_observations()
        self.compute_reward(self.actions)

